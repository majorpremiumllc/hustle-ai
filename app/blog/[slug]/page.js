export async function generateMetadata({ params }) {
    const { slug } = await params;
    const article = ARTICLES[slug];
    if (!article) return { title: "Article Not Found" };

    return {
        title: article.title,
        description: article.description,
        openGraph: {
            title: article.title,
            description: article.description,
            type: "article",
            publishedTime: article.date,
        },
        twitter: {
            title: article.title,
            description: article.description,
        },
    };
}

